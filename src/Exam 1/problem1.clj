(use 'clojure.test)

;==========================================================
(defn list-of-lists
  "Returns a new list containing n empty lists."
  [n]
  (loop [i      n
         result ()]
    (if (zero? i)
      result
      (recur (dec i) (cons () result)))))

;==========================================================
(deftest test-list-of-lists
  (is (= () (list-of-lists 0)))
  (is (= '(()) (list-of-lists 1)))
  (is (= '(() () ())
         (list-of-lists 3)))
  (is (= '(() () () () () () () () () ())
         (list-of-lists 10)))
  (is (= '(() () () () () () () () () ()
            () () () () () () () () () ()
            () () () () () () () () () ()
            () () () () () () () () () ()
            () () () () () () () () () ()
            () () () () () () () () () ()
            () () () () () () () () () ()
            () () () () () () () () () ()
            () () () () () () () () () ()
            () () () () () () () () () ())
         (list-of-lists 100))))

;==========================================================
(run-tests)
